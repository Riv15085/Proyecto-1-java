public class Motor {
    
    boolean motorOn;
    
    public Motor(){
        
    }
    
    public void Avanzar(int tiempo){
        while (tiempo>0){
        motorOn=true;
        for (int i=0; i<0; i++){
            tiempo--;
        }
        
        }  
    }
}
